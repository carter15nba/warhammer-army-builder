package myrmidia.Util.Enums;

/**
 * Enum used to describe what type of unit a unit is
 * @author Glenn Rune Strandbråten
 * @version 1.0
 */
public enum UnitType {
    /**
     * Enum value indicating that the unit type is Cavalery
     */
    Ca,
    /**
     * Enum value indicating that the unit type is Chariot
     */
    Ch,
    /**
     * Enum value indicating that the unit type is Infantry
     */
    In,
    /**
     * Enum value indicating that the unit type is Monster
     */
    Mo,
    /**
     * Enum value indicating that the unit type is Monsterous Beasts
     */
    MB,
    /**
     * Enum value indicating that the unit type is Monsterous Cavalry
     */
    MC,
    /**
     * Enum value indicating that the unit type is Monsterous Infantry
     */
    MI,
    /**
     * Enum value indicating that the unit type is Swarm
     */
    Sw,
    /**
     * Enum value indicating that the unit type is Unique
     */
    Un,
    /**
     * Enum value indicating that the unit type is War Beast
     */
    WB,
    /**
     * Enum value indicating that the unit type is War Machine
     */
    WM,
    /**
     * Enum value indicating that the unit have no unit type
     */
    _na
}
